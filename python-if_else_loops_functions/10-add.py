#!/usr/bin/python3
"""
This script defines a function to add two numbers and return the result.

Modules:
    None

Functions:
    add(a, b): Returns the sum of a and b.

Usage:
    Call the add(a, b) function with two numbers to get their sum.

Example:
    >>> add(3, 5)
    8
    >>> add(-2, 7)
    5
"""

def add(a, b):
    """
    Add two numbers and return the result.

    Args:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The sum of a and b.
    """
    return a + b
